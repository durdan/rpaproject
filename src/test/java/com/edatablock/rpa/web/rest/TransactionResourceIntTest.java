    @Test
    @Transactional
    public void createTransaction() throws Exception {
        int databaseSizeBeforeCreate = transactionRepository.findAll().size();

        // Create the Transaction
        TransactionDTO transactionDTO = transactionMapper.toDto(transaction);
        restTransactionMockMvc.perform(post("/api/transactions")
            .contentType(TestUtil.APPLICATION_JSON_UTF8)
            .content(TestUtil.convertObjectToJsonBytes(transactionDTO)))
            .andExpect(status().isCreated());

        // Validate the Transaction in the database
        List<Transaction> transactionList = transactionRepository.findAll();
        assertThat(transactionList).hasSize(databaseSizeBeforeCreate + 1);
        Transaction testTransaction = transactionList.get(transactionList.size() - 1);
        assertThat(testTransaction.getCreatedDateTime()).isEqualTo(DEFAULT_CREATED_DATE_TIME);
        assertThat(testTransaction.getStatus()).isEqualTo(DEFAULT_STATUS);
        assertThat(testTransaction.getClientEmailAddress()).isEqualTo(DEFAULT_CLIENT_EMAIL_ADDRESS);
        assertThat(testTransaction.getMessageId()).isEqualTo(DEFAULT_MESSAGE_ID);
        assertThat(testTransaction.getFileName()).isEqualTo(DEFAULT_FILE_NAME);
        assertThat(testTransaction.getProcessType()).isEqualTo(DEFAULT_PROCESS_TYPE);
        assertThat(testTransaction.getCreateDate()).isEqualTo(DEFAULT_CREATE_DATE);
        assertThat(testTransaction.getCreatedBy()).isEqualTo(DEFAULT_CREATED_BY);
        assertThat(testTransaction.getUpdateDate()).isEqualTo(DEFAULT_UPDATE_DATE);
        assertThat(testTransaction.getUpdatedBy()).isEqualTo(DEFAULT_UPDATED_BY);
    }

