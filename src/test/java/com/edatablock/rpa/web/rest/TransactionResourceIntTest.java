    @Test
    @Transactional
    public void updateTransaction() throws Exception {
        // Initialize the database
        transactionRepository.saveAndFlush(transaction);

        int databaseSizeBeforeUpdate = transactionRepository.findAll().size();

        // Update the transaction
        Transaction updatedTransaction = transactionRepository.findById(transaction.getId()).get();
        // Disconnect from session so that the updates on updatedTransaction are not directly saved in db
        em.detach(updatedTransaction);
        updatedTransaction
            .createdDateTime(UPDATED_CREATED_DATE_TIME)
            .status(UPDATED_STATUS)
            .clientEmailAddress(UPDATED_CLIENT_EMAIL_ADDRESS)
            .messageId(UPDATED_MESSAGE_ID)
            .fileName(UPDATED_FILE_NAME)
            .processType(UPDATED_PROCESS_TYPE)
            .createDate(UPDATED_CREATE_DATE)
            .createdBy(UPDATED_CREATED_BY)
            .updateDate(UPDATED_UPDATE_DATE)
            .updatedBy(UPDATED_UPDATED_BY);
        TransactionDTO transactionDTO = transactionMapper.toDto(updatedTransaction);

        restTransactionMockMvc.perform(put("/api/transactions")
            .contentType(TestUtil.APPLICATION_JSON_UTF8)
            .content(TestUtil.convertObjectToJsonBytes(transactionDTO)))
            .andExpect(status().isOk());

        // Validate the Transaction in the database
        List<Transaction> transactionList = transactionRepository.findAll();
        assertThat(transactionList).hasSize(databaseSizeBeforeUpdate);
        Transaction testTransaction = transactionList.get(transactionList.size() - 1);
        assertThat(testTransaction.getCreatedDateTime()).isEqualTo(UPDATED_CREATED_DATE_TIME);
        assertThat(testTransaction.getStatus()).isEqualTo(UPDATED_STATUS);
        assertThat(testTransaction.getClientEmailAddress()).isEqualTo(UPDATED_CLIENT_EMAIL_ADDRESS);
        assertThat(testTransaction.getMessageId()).isEqualTo(UPDATED_MESSAGE_ID);
        assertThat(testTransaction.getFileName()).isEqualTo(UPDATED_FILE_NAME);
        assertThat(testTransaction.getProcessType()).isEqualTo(UPDATED_PROCESS_TYPE);
        assertThat(testTransaction.getCreateDate()).isEqualTo(UPDATED_CREATE_DATE);
        assertThat(testTransaction.getCreatedBy()).isEqualTo(UPDATED_CREATED_BY);
        assertThat(testTransaction.getUpdateDate()).isEqualTo(UPDATED_UPDATE_DATE);
        assertThat(testTransaction.getUpdatedBy()).isEqualTo(UPDATED_UPDATED_BY);
    }

